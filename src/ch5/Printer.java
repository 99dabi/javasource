package ch5;

public class Printer {
	//기본생성자-생성자가 하나도 없으면 자동으로 생성
	void println(int arg){
		System.out.println(arg);
	}
	void println(boolean arg){
		System.out.println(arg);	
	}
	void println(double arg){
		System.out.println(arg);
	}
	void println(String arg){
		System.out.println(arg);
	}
	void println(char arg){
		System.out.println(arg);
	}
}
